package JavaPrograms;

public class DataTypesTest {

	public static void main(String[] args) {
		
		// Primitive data types
		
		// 1.byte
		byte a = 12;
		byte b = 120;
		System.out.println(a+b);
		
		// 2.short
		short c = 356;
		short d = 7800;
		System.out.println(c*d);
		
		// 3.Int
		int e = 23000;
		int f = 45;
		System.out.println(f-e);
		
		// 4.Long
		long g = 67000;
		long h = 45000;
		System.out.println(g*h);
		
		// 5.float
		float i = 56.7f;
		float j = 1209.67f;
		System.out.println(i*j);
		
		// 6.double
		double k = 345.78;
		double l = 67.90;
		System.out.println(k*l);
		
		// 7.char
		char m = 'A';
		char n = 'G';
		System.out.println("Letters: "+ m+n);
		
		// 8.boolean
		boolean o = false;
		boolean p = true;
		System.out.print(o+" ");
		System.out.print(p);
		
		
		// non primitive data types
		
		// Classes, Interface, Arrays and Strings
		
		

	}

}
